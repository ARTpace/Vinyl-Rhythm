const t2s_dict: { [key: string]: string } = Object.fromEntries(
  Object.entries(s2t_dict).map(([s, t]) => [t, s])
);

/**
 * 简体转繁体
 */
export const s2t = (str: string): string => {
  return str.split('').map(char => s2t_dict[char] || char).join('');
};

/**
 * 繁体转简体
 */
export const t2s = (str: string): string => {
  return str.split('').map(char => t2s_dict[char] || char).join('');
};

/**
 * 将字符串统一规范化（转为简体并在搜索时匹配）
 */
export const normalizeChinese = (str: string): string => {
  return t2s(str).toLowerCase();
};
